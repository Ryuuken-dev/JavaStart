package CalculateUnitsApp;

public class CalculateUnits {

    public double metersToCentimeters(double meters){
        return meters / 0.01;
    }

    public double metersToMilimeters(double meters){
        return meters / 0.001;
    }

    public double centimetersToMeters(double cm){
        return cm * 0.01;
    }

    public double milimetersToMeters(double mm){
        return mm * 0.001;
    }

}
